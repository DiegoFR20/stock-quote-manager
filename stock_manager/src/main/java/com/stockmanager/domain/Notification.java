package com.stockmanager.domain;

public class Notification {
	private String host, port;

	public String getHost() {
		return host;
	}

	public String getPort() {
		return port;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public void setPort(String port) {
		this.port = port;
	}
	
}
